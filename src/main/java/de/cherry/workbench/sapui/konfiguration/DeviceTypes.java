package de.cherry.workbench.sapui.konfiguration;

public class DeviceTypes
{
  private boolean desktop;

  public boolean getDesktop() { return this.desktop; }

  public void setDesktop(boolean desktop) { this.desktop = desktop; }

  private boolean tablet;

  public boolean getTablet() { return this.tablet; }

  public void setTablet(boolean tablet) { this.tablet = tablet; }

  private boolean phone;

  public boolean getPhone() { return this.phone; }

  public void setPhone(boolean phone) { this.phone = phone; }
}
